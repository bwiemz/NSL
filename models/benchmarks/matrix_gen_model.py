#!/usr/bin/env python3
"""Generate parameterized NSL packed-pretraining benchmark programs.

Every generated program:
  * builds a GQA transformer (params: d_model, n_blocks, nh, nkv, hd, ffn, vocab)
  * packs a deterministic token stream (doc pattern: fixed length or a
    lognormal-drawn cycle of lengths)
  * trains with AdamW + grad accumulation + clip on the GPU
  * prints one loss line + one clock() line per micro-batch between markers
    (phase lines come from NSL_PHASE_TIMING=1 at run time)
"""

# 16 doc lengths drawn offline from lognormal(median=180, sigma=0.9), clipped
# to [16, 2000], then frozen here for reproducibility. Sum = 4864.
REALISTIC_DOC_LENGTHS = [37, 251, 89, 512, 143, 23, 401, 178, 66, 730, 204, 31,
                         956, 118, 305, 820]


def doc_stream_code(pattern, seq_len, batch_size, n_microbatches):
    """Return (nsl_code, total_tokens, doc_lengths_cycle) building `tokens`.

    pattern: int (fixed doc length, includes its trailing separator)
             or "realistic" or "single" (one doc per row: no separators).
    """
    total_needed = seq_len * batch_size * n_microbatches
    if pattern == "single":
        # No separators at all: every packed row is one document.
        n = total_needed
        code = (
            f"let unit = arange(4095) + full([4095], 1.0)\n"
            f"let reps = {n // 4095 + 1}\n"
            f"let tokens = unit.reshape([1, 4095]).expand([reps, 4095])"
            f".contiguous().reshape([reps * 4095])\n"
        )
        return code, total_needed, [seq_len]
    if pattern == "realistic":
        lengths = REALISTIC_DOC_LENGTHS
        unit_len = sum(lengths)
        reps = total_needed // unit_len + 1
        pieces = []
        for i, doc_len in enumerate(lengths):
            body = doc_len - 1
            # Tokens cycle 1..min(body,4000); vocab floor handled by caller.
            pieces.append(
                f"tensor_cat([arange({body}) + full([{body}], 1.0), zeros([1])], 0)"
            )
        joined = ", ".join(pieces)
        code = (
            f"let sub = tensor_cat([{joined}], 0)\n"
            f"let tokens = sub.reshape([1, {unit_len}]).expand([{reps}, {unit_len}])"
            f".contiguous().reshape([{reps * unit_len}])\n"
        )
        return code, total_needed, lengths
    # Fixed doc length (with trailing separator inside the length).
    doc_len = int(pattern)
    body = doc_len - 1
    reps = total_needed // doc_len + 1
    code = (
        f"let unit = tensor_cat([arange({body}) + full([{body}], 1.0), zeros([1])], 0)\n"
        f"let tokens = unit.reshape([1, {doc_len}]).expand([{reps}, {doc_len}])"
        f".contiguous().reshape([{reps * doc_len}])\n"
    )
    return code, total_needed, [doc_len]


def gen(cfg):
    """cfg keys: name d_model n_blocks nh nkv hd ffn vocab seq batch accum
    n_microbatches doc_pattern lr save_path moment_precision(ignored here)"""
    d = cfg
    dm, nh, nkv, hd, ffn = d["d_model"], d["nh"], d["nkv"], d["hd"], d["ffn"]
    assert nh * hd == dm, f"nh*hd must equal d_model ({nh}*{hd} != {dm})"
    stream_code, total_tokens, _ = doc_stream_code(
        d["doc_pattern"], d["seq"], d["batch"], d["n_microbatches"]
    )
    init = 0.02
    blocks = d["n_blocks"]
    if d.get("optimizer") == "sgd_momentum":
        opt_line = f"SGD(lr={d['lr']}, momentum=0.9)"
    else:
        opt_line = (f"AdamW(lr={d['lr']}, weight_decay=0.01, beta1=0.9, "
                    f"beta2=0.95, eps=1e-8)")
    prog = f"""# AUTOGENERATED benchmark config: {d['name']}
from nsl.nn.gqa import GroupedQueryAttention
from nsl.nn.losses import cross_entropy

model Blk:
    norm_a: Tensor = ones([{dm}])
    norm_f: Tensor = ones([{dm}])
    attn: GroupedQueryAttention = GroupedQueryAttention({dm}, {nh}, {nkv}, {hd}, 0.0)
    w_up: Tensor = randn([{dm}, {ffn}]) * {init}
    w_down: Tensor = randn([{ffn}, {dm}]) * {init}

    fn forward(self, h: Tensor, mask: Tensor, seg: Tensor, pos: Tensor, training: bool) -> Tensor:
        let a = self.attn.forward_packed(rmsnorm(h, self.norm_a, 0.00001), mask, seg, pos, training)
        let h1 = h + a
        let f = (rmsnorm(h1, self.norm_f, 0.00001) @ self.w_up) @ self.w_down
        return h1 + f

model PackedLM:
    embed: Tensor = randn([{d['vocab']}, {dm}]) * {init}
    blocks: [Blk; {blocks}] = Blk()
    norm_out: Tensor = ones([{dm}])

    fn forward_train(self, input_ids: Tensor, mask: Tensor, seg: Tensor, pos: Tensor, training: bool) -> Tensor:
        let ids_shape = input_ids.shape
        let batch_size = ids_shape[0]
        let seq_len = ids_shape[1]
        let flat_ids = input_ids.reshape([batch_size * seq_len])
        let emb = embedding_lookup(self.embed, flat_ids)
        let h = emb.reshape([batch_size, seq_len, {dm}])
        for block in self.blocks:
            h = block.forward(h, mask, seg, pos, training)
        let hn = rmsnorm(h, self.norm_out, 0.00001)
        return hn @ self.embed.transpose(0, 1)

let m = PackedLM()
m.to(cuda)

{stream_code}
let loader = DataLoader(tokens, batch_size={d['batch']}, seq_len={d['seq']}, shuffle=false, drop_last=true, packing=true, pack_separator=0)

print("LOSS_STREAM_BEGIN")

train(model=m, epochs=1, grad_accumulation={d['accum']}, grad_clip=1.0):
    optimizer: {opt_line}
    step(batch):
        let logits = m.forward_train(batch.input_ids, batch.attention_mask, batch.segment_ids, batch.position_ids, false)
        let ls = logits.shape
        let flat_logits = logits.reshape([ls[0] * ls[1], ls[2]])
        let flat_labels = batch.labels.reshape([ls[0] * ls[1]])
        let loss = cross_entropy(flat_logits, flat_labels)
    callbacks:
        on_step(step, loss):
            print(loss)
            print(clock())

print("LOSS_STREAM_END")
"""
    return prog


if __name__ == "__main__":
    import json, sys
    cfg = json.loads(sys.argv[1])
    sys.stdout.write(gen(cfg))
